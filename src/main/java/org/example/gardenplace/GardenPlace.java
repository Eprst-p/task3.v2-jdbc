package org.example.gardenplace;

public class GardenPlace {

    private long id;
    private String location;
    private int carrot;
    private int potato;
    private int cabbage;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public int getCarrot() {
        return carrot;
    }

    public void setCarrot(int carrot) {
        this.carrot = carrot;
    }

    public int getPotato() {
        return potato;
    }

    public void setPotato(int potato) {
        this.potato = potato;
    }

    public int getCabbage() {
        return cabbage;
    }

    public void setCabbage(int cabbage) {
        this.cabbage = cabbage;
    }

}
